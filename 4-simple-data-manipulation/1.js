// + -> adds two numbers
// - -> subtracts one number from another
// / -> divides one number from another
// * -> multiplies two numbers
// % -> divides one number by another and returns the remainder
// == -> compares equality of a number or string
// <, >, <=, >= -> compares greater than, equal to, etc.

var num1 = 10
var num2 = 2

console.log(num1 + num2) // should return 12
console.log(num1 - num2) // should return 8
console.log(num1 * num2) // should return 20
console.log(num1 / num2) // should return 5
console.log(num1 % num2) // should return 0
console.log(num1 == num2) // should return false
console.log(num1 < num2) // should return false
console.log(num1 > num2) // should return true
console.log(num1 <= num2) // should return false
console.log(num1 >= num2) // should return true