// Learn how to create a multi-dimensional array. Learn how to access elements within the multidimensional array.

var array = [[1, 2, 3], ["a", "b", "c"]]

console.log(array[0] + " " + array[1]); // this should return the multi-dimensional array
console.log(array[1][2]); // this should return "c"
console.log(array[0][0]); // this should return 1